def getInverseOrthographicProjection(img, step=2):
    """
    Convert one orthographic face image (already loaded as an RGB array)
    into sampled points on the upper hemisphere in local face coordinates.

    Parameters
    ----------
    img : np.ndarray
        RGB image array of shape (H, W, 3).
    step : int
        Pixel stride for downsampling during sampling.

    Returns
    -------
    U, V, W : np.ndarray
        Local sphere coordinates on the face.
    COLORS : np.ndarray
        RGB colors scaled to [0, 1].
    WEIGHTS : np.ndarray
        Blending weights, larger near the image center.
    """
    height_img, width_img, _ = img.shape

    U, V, W = [], [], []
    COLORS = []
    WEIGHTS = []

    for i in range(0, width_img, step):
        for j in range(0, height_img, step):

            # Map pixel coordinates to [-1, 1] x [-1, 1]
            x_img = (2 * i / (width_img - 1)) - 1
            y_img = (2 * j / (height_img - 1)) - 1

            # Flip y-axis so positive is upward
            y_img = -y_img

            # Restrict to unit disk
            if x_img**2 + y_img**2 > 1:
                continue

            # Inverse orthographic projection onto unit sphere
            u = x_img
            v = y_img
            w = math.sqrt(max(0, 1 - (u**2 + v**2)))

            U.append(u)
            V.append(v)
            W.append(w)

            COLORS.append(img[j, i] / 255.0)
            WEIGHTS.append(w**2)

    return (
        np.array(U),
        np.array(V),
        np.array(W),
        np.array(COLORS),
        np.array(WEIGHTS)
    )


def getGlobalSphereCoords(U, V, W, view):
    """
    Convert local face coordinates (U, V, W) into global sphere coordinates
    based on which cube/face view the image represents.
    """
    if view == "front":
        return U, -W, V
    elif view == "back":
        return -U, W, V
    elif view == "left":
        return -W, -U, V
    elif view == "right":
        return W, U, V
    elif view == "top":
        return U, V, W
    elif view == "bottom":
        return -U, V, -W
    else:
        raise ValueError(f"Unknown view: {view}")


def process_one_face(img, view, step=2):
    """
    Process one face image:
    image -> local sphere coords -> global sphere coords
    """
    U, V, W, colors_face, weights_face = getInverseOrthographicProjection(img, step=step)
    X, Y, Z = getGlobalSphereCoords(U, V, W, view)
    return X, Y, Z, colors_face, weights_face


def constructFullSphereFromSixImages(top, bottom, front, back, left, right, step=2):
    """
    Reconstruct a full sphere by combining six orthographic images.

    Parameters
    ----------
    top, bottom, front, back, left, right : np.ndarray
        RGB image arrays for the six faces.
    step : int
        Pixel stride used during inverse orthographic sampling.

    Returns
    -------
    X, Y, Z : np.ndarray
        Global sphere coordinates.
    COLORS : np.ndarray
        RGB colors.
    WEIGHTS : np.ndarray
        Weights for later blending.
    """
    view_images = {
        "front": front,
        "back": back,
        "left": left,
        "right": right,
        "top": top,
        "bottom": bottom,
    }

    views = ["front", "back", "left", "right", "top", "bottom"]

    X_list, Y_list, Z_list = [], [], []
    COLORS_list, WEIGHTS_list = [], []

    for view in views:
        img = view_images[view]
        X, Y, Z, colors_face, weights_face = process_one_face(img, view, step=step)

        X_list.append(X)
        Y_list.append(Y)
        Z_list.append(Z)
        COLORS_list.append(colors_face)
        WEIGHTS_list.append(weights_face)

    X = np.concatenate(X_list)
    Y = np.concatenate(Y_list)
    Z = np.concatenate(Z_list)
    COLORS = np.concatenate(COLORS_list)
    WEIGHTS = np.concatenate(WEIGHTS_list)

    return X, Y, Z, COLORS, WEIGHTS


def blendSphereColors(X, Y, Z, COLORS, WEIGHTS, decimals=3):
    """
    Blend overlapping sphere points using weighted averaging after binning
    nearby points by rounded coordinates.
    """
    bins = {}

    for x, y, z, color, weight in zip(X, Y, Z, COLORS, WEIGHTS):
        key = (round(x, decimals), round(y, decimals), round(z, decimals))

        if key not in bins:
            bins[key] = {
                "xyz_sum": np.array([0.0, 0.0, 0.0]),
                "color_sum": np.array([0.0, 0.0, 0.0]),
                "weight_sum": 0.0
            }

        bins[key]["xyz_sum"] += weight * np.array([x, y, z])
        bins[key]["color_sum"] += weight * color
        bins[key]["weight_sum"] += weight

    X_blend, Y_blend, Z_blend, COLORS_blend = [], [], [], []

    for entry in bins.values():
        wsum = entry["weight_sum"]
        xyz_avg = entry["xyz_sum"] / wsum
        color_avg = entry["color_sum"] / wsum

        X_blend.append(xyz_avg[0])
        Y_blend.append(xyz_avg[1])
        Z_blend.append(xyz_avg[2])
        COLORS_blend.append(color_avg)

    return (
        np.array(X_blend),
        np.array(Y_blend),
        np.array(Z_blend),
        np.array(COLORS_blend)
    )


def stereographicallyProjectFromSphere(X, Y, Z, COLORS, z_max=0.9):
    """
    Stereographic projection from the north pole onto the plane.
    Points too close to the north pole are skipped.
    """
    X_stereo, Y_stereo, COLORS_stereo = [], [], []

    for x, y, z, color in zip(X, Y, Z, COLORS):
        if z > z_max:
            continue

        x_s = -2 * x / (z - 1)
        y_s = -2 * y / (z - 1)

        X_stereo.append(x_s)
        Y_stereo.append(y_s)
        COLORS_stereo.append(color)

    return np.array(X_stereo), np.array(Y_stereo), np.array(COLORS_stereo)


def stereographicallyProjectFromSphereSouth(X, Y, Z, COLORS, z_min=-0.9):
    """
    Stereographic projection from the south pole onto the plane.
    Points too close to the south pole are skipped.
    """
    X_stereo2, Y_stereo2, COLORS_stereo2 = [], [], []

    for x, y, z, color in zip(X, Y, Z, COLORS):
        if z < z_min:
            continue

        x_s = 2 * x / (z + 1)
        y_s = 2 * y / (z + 1)

        X_stereo2.append(x_s)
        Y_stereo2.append(y_s)
        COLORS_stereo2.append(color)

    return np.array(X_stereo2), np.array(Y_stereo2), np.array(COLORS_stereo2)


def projectCylindricalLambert(X, Y, Z, COLORS):
    """
    Lambert cylindrical-style parameterization:
    longitude lambda and height z.
    """
    X_lambert, Y_lambert, COLORS_lambert = [], [], []

    for x, y, z, color in zip(X, Y, Z, COLORS):
        lam = math.atan2(y, x)
        t = z

        X_lambert.append(lam)
        Y_lambert.append(t)
        COLORS_lambert.append(color)

    return np.array(X_lambert), np.array(Y_lambert), np.array(COLORS_lambert)

