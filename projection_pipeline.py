def render_stereographic_image(Xs, Ys, COLORS,
                               grid_res=800,
                               method='linear',
                               r_max=None,   # 👈 NEW
                               title="Stereographic Projection",
                               prefix="object",
                               suffix="interpolated",
                               save=True,
                               folder="outputs"):

    # -------------------------
    # OPTIONAL RADIAL CROP
    # -------------------------
    if r_max is not None:
        R = np.sqrt(Xs**2 + Ys**2)
        mask = R <= r_max

        Xs = Xs[mask]
        Ys = Ys[mask]
        COLORS = COLORS[mask]

    # bounding box of (possibly cropped) points
    x_min, x_max = Xs.min(), Xs.max()
    y_min, y_max = Ys.min(), Ys.max()

    # -------------------------
    # GRID
    # -------------------------
    xi = np.linspace(x_min, x_max, grid_res)
    yi = np.linspace(y_min, y_max, grid_res)
    XI, YI = np.meshgrid(xi, yi)

    # -------------------------
    # INTERPOLATION
    # -------------------------
    Rch = griddata((Xs, Ys), COLORS[:, 0], (XI, YI), method=method)
    Gch = griddata((Xs, Ys), COLORS[:, 1], (XI, YI), method=method)
    Bch = griddata((Xs, Ys), COLORS[:, 2], (XI, YI), method=method)

    img = np.dstack([Rch, Gch, Bch])

    # fill holes with nearest
    Rn = griddata((Xs, Ys), COLORS[:, 0], (XI, YI), method='nearest')
    Gn = griddata((Xs, Ys), COLORS[:, 1], (XI, YI), method='nearest')
    Bn = griddata((Xs, Ys), COLORS[:, 2], (XI, YI), method='nearest')

    img_near = np.dstack([Rn, Gn, Bn])

    nan_mask = np.isnan(img)
    img[nan_mask] = img_near[nan_mask]

    img = np.clip(img, 0, 1)

    # -------------------------
    # PLOT
    # -------------------------
    plt.figure(figsize=(8, 8))
    plt.imshow(img, origin='lower', extent=[x_min, x_max, y_min, y_max])
    plt.gca().set_aspect('equal')
    plt.title(f"{prefix} - {title}")
    plt.xlabel("x")
    plt.ylabel("y")

    if save:
        folder = f"{folder}_{prefix}"
        os.makedirs(folder, exist_ok=True)

        filename = f"{prefix}_{suffix}.png"
        filepath = os.path.join(folder, filename)

        plt.imsave(filepath, img)
        print(f"Saved: {filepath}")

    plt.show()

    return img

def render_lambert_image(Xs, Ys, COLORS,
                         grid_res_x=1200,
                         grid_res_y=600,
                         method='linear',
                         seam_pad=0.15,
                         title="Lambert Cylindrical Projection",
                         prefix="object",
                         suffix="lambert_interp",
                         save=True,
                         folder="outputs"):

    # original arrays
    Xs = np.asarray(Xs)
    Ys = np.asarray(Ys)
    COLORS = np.asarray(COLORS)

    # duplicate points near seam so interpolation wraps better
    X_aug = [Xs]
    Y_aug = [Ys]
    C_aug = [COLORS]

    left_mask = Xs < (-np.pi + seam_pad)
    right_mask = Xs > (np.pi - seam_pad)

    if np.any(left_mask):
        X_aug.append(Xs[left_mask] + 2*np.pi)
        Y_aug.append(Ys[left_mask])
        C_aug.append(COLORS[left_mask])

    if np.any(right_mask):
        X_aug.append(Xs[right_mask] - 2*np.pi)
        Y_aug.append(Ys[right_mask])
        C_aug.append(COLORS[right_mask])

    X_all = np.concatenate(X_aug)
    Y_all = np.concatenate(Y_aug)
    C_all = np.concatenate(C_aug)

    # fixed Lambert domain
    xi = np.linspace(-np.pi, np.pi, grid_res_x)
    yi = np.linspace(-1, 1, grid_res_y)
    XI, YI = np.meshgrid(xi, yi)

    # interpolate RGB channels
    R = griddata((X_all, Y_all), C_all[:, 0], (XI, YI), method=method)
    G = griddata((X_all, Y_all), C_all[:, 1], (XI, YI), method=method)
    B = griddata((X_all, Y_all), C_all[:, 2], (XI, YI), method=method)

    img = np.dstack([R, G, B])

    # fill holes with nearest neighbor
    Rn = griddata((X_all, Y_all), C_all[:, 0], (XI, YI), method='nearest')
    Gn = griddata((X_all, Y_all), C_all[:, 1], (XI, YI), method='nearest')
    Bn = griddata((X_all, Y_all), C_all[:, 2], (XI, YI), method='nearest')
    img_near = np.dstack([Rn, Gn, Bn])

    mask = np.isnan(img)
    img[mask] = img_near[mask]

    img = np.clip(img, 0, 1)

    plt.figure(figsize=(12, 6))
    plt.imshow(img, origin='lower', extent=[-np.pi, np.pi, -1, 1], aspect='auto')
    plt.title(f"{prefix} - {title}")
    plt.xlabel("Longitude (λ)")
    plt.ylabel("Height (z)")

    if save:
        folder = f"{folder}_{prefix}"
        os.makedirs(folder, exist_ok=True)

        # save raw image (correct orientation)
        filepath_img = os.path.join(folder, f"{prefix}_{suffix}_raw.png")
        plt.imsave(filepath_img, np.flipud(img), vmin=0, vmax=1)
        print(f"Saved raw image: {filepath_img}")

        # save plotted version (with axes, labels, etc.)
        filepath_plot = os.path.join(folder, f"{prefix}_{suffix}_plot.png")
        plt.savefig(filepath_plot, dpi=200, bbox_inches='tight')
        print(f"Saved plot: {filepath_plot}")

    plt.show()
    plt.close()

    return img

from tqdm.notebook import tqdm

class ProjectionPipeline:
    def __init__(self, image_collection):
        """
        Store the ImageCollection so the pipeline can access all grouped objects.
        """
        self.image_collection = image_collection

    def process_one_object(self, prefix, size=None, step=2, blend=True):
        """
        Process one object from its six input images into a reconstructed sphere.

        Parameters
        ----------
        prefix : str
            Object name/prefix, e.g. "testball".
        size : int or None
            If not None, resize each input face image to (size, size) before processing.
        step : int
            Pixel stride used during inverse orthographic sampling.
            Larger step means fewer sampled pixels and faster runtime.
        blend : bool
            Whether to blend overlapping points from different faces.

        Returns
        -------
        result : dict
            Dictionary containing reconstructed coordinates, colors, and metadata.
        """
        obj = self.image_collection.get_object(prefix)

        # Load the six face images in a fixed order
        front, back, left, right, top, bottom = obj.load_images_in_order(size=size)

        # Reconstruct the full sphere from the six faces
        X, Y, Z, COLORS, WEIGHTS = constructFullSphereFromSixImages(
            top, bottom, front, back, left, right, step=step
        )

        # Blend overlapping regions if requested
        # decimals=2 matches the smoother behavior from your older pipeline
        if blend:
            X, Y, Z, COLORS = blendSphereColors(X, Y, Z, COLORS, WEIGHTS, decimals=2)
            WEIGHTS = None

        return {
            "prefix": prefix,
            "X": X,
            "Y": Y,
            "Z": Z,
            "COLORS": COLORS,
            "WEIGHTS": WEIGHTS,
            "step": step,
            "size": size,
            "blend": blend
        }

    def process_all_objects(self, size=None, step=2, blend=True, show_progress=True):
        """
        Process all objects in the ImageCollection.

        Parameters
        ----------
        size : int or None
            Optional resize dimension for each face image.
        step : int
            Pixel stride used during inverse orthographic sampling.
        blend : bool
            Whether to blend overlapping face contributions.
        show_progress : bool
            Whether to show a tqdm progress bar.

        Returns
        -------
        results : dict
            Dictionary mapping each prefix to its processed result dictionary.
        """
        results = {}
        prefixes = self.image_collection.get_prefixes()

        iterable = tqdm(prefixes, desc="Processing objects") if show_progress else prefixes

        for prefix in iterable:
            try:
                if show_progress:
                    iterable.set_postfix_str(prefix)

                results[prefix] = self.process_one_object(
                    prefix, size=size, step=step, blend=blend
                )
            except Exception as e:
                print(f"Skipping {prefix}: {e}")

        return results

    def plot_3d_result(self, result, save=True, folder="outputs", max_points=1000000):
        """
        Plot the reconstructed sphere as a 3D colored point cloud.
        """
        suffix = f"raw_step{result['step']}"

        plot_points_3d_views(
            result["X"],
            result["Y"],
            result["Z"],
            result["COLORS"],
            prefix=result["prefix"],
            suffix=suffix,
            save=save,
            folder=folder,
            max_points=max_points
        )

    # -------------------------
    # NORTH-POLE STEREOGRAPHIC
    # -------------------------

    def plot_stereographic_north_result(self, result, save=True, folder="outputs", z_max=0.75):
        """
        Plot the north-pole stereographic projection.

        Projecting FROM the north pole places the south pole near the center.
        """
