def plot_points_3d_views(X, Y, Z, COLORS,
                         title="3D sphere",
                         prefix="object",
                         suffix="raw",
                         save=True,
                         folder="outputs",
                         max_points=500000,
                         dpi=200,
                         point_size=1,
                         views=None):
    """
    Plot a colored 3D point cloud of a sphere from several viewing angles.
    """

    if not (len(X) == len(Y) == len(Z) == len(COLORS)):
        raise ValueError("X, Y, Z, and COLORS must all have the same length.")

    n = len(X)
    print("Total points:", n)

    # randomly sample if too large
    if n > max_points:
        idx = np.random.choice(n, size=max_points, replace=False)
        X = X[idx]
        Y = Y[idx]
        Z = Z[idx]
        COLORS = COLORS[idx]
        print(f"Downsampled to {max_points} points")

    # default views
    if views is None:
        views = [
            (20,   30,  "front_right"),
            (20,  120,  "front_left"),
            (20, -60,   "back_right"),
            (20, -150,  "back_left"),
            (90,   0,   "top"),
            (-90,  0,   "bottom"),
        ]

    if save:
        folder = f"{folder}_{prefix}"
        os.makedirs(folder, exist_ok=True)

    for elev, azim, view_label in views:
        fig = plt.figure(figsize=(8, 8))
        ax = fig.add_subplot(111, projection='3d')

        ax.scatter(X, Y, Z, c=COLORS, s=point_size, depthshade=False)

        ax.set_box_aspect([1, 1, 1])
        ax.set_xlabel("X")
        ax.set_ylabel("Y")
        ax.set_zlabel("Z")

        full_title = f"{prefix} - {title} ({view_label})"
        ax.set_title(full_title)
        ax.view_init(elev=elev, azim=azim)

        if save:
            filename = f"{prefix}_{suffix}_3D_{view_label}.png"
            filepath = os.path.join(folder, filename)
            fig.savefig(filepath, dpi=dpi, bbox_inches='tight')
            print(f"Saved: {filepath}")

        plt.show()
        plt.close()


