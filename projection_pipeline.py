EXPECTED_VIEWS = {"front", "back", "left", "right", "top", "bottom"}

# low-level utilities

def normalize_filename(filename):
    return filename.lower().replace("-", "_").strip()


def extract_prefix_and_view(filepath):
    fname = os.path.basename(filepath)
    stem, _ = os.path.splitext(fname)
    stem = normalize_filename(stem)

    parts = stem.split("_")
    if len(parts) < 2:
        raise ValueError(f"Could not parse prefix/view from filename: {fname}")

    view = parts[-1]
    prefix = "_".join(parts[:-1])

    if view not in EXPECTED_VIEWS:
        raise ValueError(f"Unrecognized view '{view}' in filename: {fname}")

    return prefix, view


def load_and_resize(filepath, size=None):
    img = cv2.imread(filepath)
    if img is None:
        raise FileNotFoundError(f"Could not load image: {filepath}")

    img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)

    if size is not None:
        img = cv2.resize(img, (size, size))

    return img

# data model

class ObjectImageSet:
    def __init__(self, path, prefix, files_by_view=None):
        self.path = path
        self.prefix = prefix
        self.files_by_view = {} if files_by_view is None else files_by_view

    def add_file(self, filepath):
        prefix, view = extract_prefix_and_view(filepath)

        if prefix != self.prefix:
            raise ValueError(f"File {filepath} does not match prefix {self.prefix}")

        self.files_by_view[view] = filepath

    def has_all_six_views(self):
        return EXPECTED_VIEWS.issubset(set(self.files_by_view.keys()))

    def missing_views(self):
        return EXPECTED_VIEWS - set(self.files_by_view.keys())

    def validate(self):
        if not self.has_all_six_views():
            raise ValueError(
                f"Object '{self.prefix}' is missing views: {sorted(self.missing_views())}"
            )

    def load_images(self, size=None):
        images = {}

        for view, filepath in self.files_by_view.items():
            images[view] = load_and_resize(filepath, size=size)

        return images

    def load_images_in_order(self, size=None):
        self.validate()
        images = self.load_images(size=size)

        return (
            images["front"],
            images["back"],
            images["left"],
            images["right"],
            images["top"],
            images["bottom"],
        )

class ImageCollection:
    def __init__(self, path=""):
        self.path = path
        self.objects = {}

    def add_file(self, filepath):
        prefix, view = extract_prefix_and_view(filepath)

        if prefix not in self.objects:
            self.objects[prefix] = ObjectImageSet(self.path, prefix)

        self.objects[prefix].add_file(filepath)

    def add_files(self, filepaths):
        for filepath in filepaths:
            self.add_file(filepath)

    def get_prefixes(self):
        return list(self.objects.keys())

    def get_object(self, prefix):
        return self.objects[prefix]

    def validate_all(self):
        for obj in self.objects.values():
            obj.validate()

